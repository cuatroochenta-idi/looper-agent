func TestMeHandlerEnabled(t *testing.T) {
	a := NewAuth("admin", "pw", "secret", "tok")

	// Unauthenticated.
	rec := httptest.NewRecorder()
	a.MeHandler(rec, httptest.NewRequest(http.MethodGet, "/api/me", nil))
	var resp meResponse
	_ = json.NewDecoder(rec.Body).Decode(&resp)
	if !resp.AuthEnabled || resp.Authenticated {
		t.Errorf("unauth: %+v, want enabled:true authenticated:false", resp)
	}

	// Authenticated via cookie.
	loginRec := login(t, a, "pw")
	var session *http.Cookie
	for _, c := range loginRec.Result().Cookies() {
		if c.Name == sessionCookieName {
			session = c
		}
	}
	req := httptest.NewRequest(http.MethodGet, "/api/me", nil)
	req.AddCookie(session)
	rec2 := httptest.NewRecorder()
	a.MeHandler(rec2, req)
	var resp2 meResponse
	_ = json.NewDecoder(rec2.Body).Decode(&resp2)
	if !resp2.Authenticated || resp2.Username != "admin" {
		t.Errorf("auth: %+v, want authenticated:true username:admin", resp2)
	}
}

func TestLogoutClearsCookie(t *testing.T) {
	a := NewAuth("admin", "pw", "secret", "tok")
	rec := httptest.NewRecorder()
	a.LogoutHandler(rec, httptest.NewRequest(http.MethodPost, "/api/logout", nil))
	if rec.Code != http.StatusNoContent {
		t.Fatalf("code = %d, want 204", rec.Code)
	}
	var session *http.Cookie
	for _, c := range rec.Result().Cookies() {
		if c.Name == sessionCookieName {
			session = c
		}
	}
	if session == nil {
		t.Fatal("logout should set an (expiring) session cookie")
	}
	if session.Value != "" || !session.Expires.Before(time.Now()) {
		t.Errorf("logout cookie not expired: %+v", session)
	}
}

func TestTamperedCookieRejected(t *testing.T) {
	a := NewAuth("admin", "pw", "secret", "tok")
	if a.validSession("v1:9999999999:deadbeef") {
		t.Error("forged HMAC should be rejected")
	}
	if a.validSession("v1:1:" + a.mac("1")) {
		t.Error("expired session should be rejected")
	}
	if a.validSession("garbage") {
		t.Error("malformed value should be rejected")
	}
}
