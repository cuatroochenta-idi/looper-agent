func TestRateLimitTrips(t *testing.T) {
	a := NewAuth("admin", "pw", "secret", "tok")
	// 5 attempts/min allowed; 6th trips regardless of correctness.
	for i := 0; i < 5; i++ {
		rec := login(t, a, "wrong")
		if rec.Code != http.StatusUnauthorized {
			t.Fatalf("attempt %d: code = %d, want 401", i, rec.Code)
		}
	}
	rec := login(t, a, "wrong")
	if rec.Code != http.StatusTooManyRequests {
		t.Fatalf("6th attempt: code = %d, want 429", rec.Code)
	}
}

func TestRateLimitPerIP(t *testing.T) {
	rl := newRateLimiter(2, time.Minute)
	for i := 0; i < 2; i++ {
		if !rl.allow("a") {
			t.Fatalf("ip a attempt %d should be allowed", i)
		}
	}
	if rl.allow("a") {
		t.Fatal("ip a 3rd should be blocked")
	}
	if !rl.allow("b") {
		t.Fatal("ip b should have its own window")
	}
}

