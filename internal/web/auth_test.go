func TestMiddlewareBlocksWithoutCookie(t *testing.T) {
	a := NewAuth("admin", "pw", "secret", "tok")

	req := httptest.NewRequest(http.MethodGet, "/api/state/runs", nil)
	rec := httptest.NewRecorder()
	a.Middleware(okHandler).ServeHTTP(rec, req)
	if rec.Code != http.StatusUnauthorized {
		t.Fatalf("code = %d, want 401", rec.Code)
	}
	var body map[string]string
	if err := json.NewDecoder(rec.Body).Decode(&body); err != nil {
		t.Fatalf("decode: %v", err)
	}
	if body["error"] != "unauthorized" {
		t.Errorf("body = %v, want error:unauthorized", body)
	}

	// SPA shell GETs pass without a session — the bundle carries no run
	// data and the browser needs it to render the login screen at all.
	req2 := httptest.NewRequest(http.MethodGet, "/runs", nil)
	rec2 := httptest.NewRecorder()
	a.Middleware(okHandler).ServeHTTP(rec2, req2)
	if rec2.Code != http.StatusOK {
		t.Fatalf("SPA shell GET = %d, want 200 (must render login screen)", rec2.Code)
	}

	// Non-GET on a non-API path is still gated (bare 401, no JSON body).
	req3 := httptest.NewRequest(http.MethodPost, "/runs", nil)
	rec3 := httptest.NewRecorder()
	a.Middleware(okHandler).ServeHTTP(rec3, req3)
	if rec3.Code != http.StatusUnauthorized {
		t.Fatalf("non-api POST code = %d, want 401", rec3.Code)
	}
	if rec3.Body.Len() != 0 {
		t.Errorf("non-api 401 should have empty body, got %q", rec3.Body.String())
	}
}

func TestIngestBearer(t *testing.T) {
	a := NewAuth("admin", "pw", "secret", "secret-token")
	h := a.Middleware(okHandler)

	// Correct bearer passes.
	req := httptest.NewRequest(http.MethodPost, "/ingest", strings.NewReader("{}"))
	req.Header.Set("Authorization", "Bearer secret-token")
	rec := httptest.NewRecorder()
	h.ServeHTTP(rec, req)
	if rec.Code != http.StatusOK {
		t.Fatalf("valid bearer: code = %d, want 200", rec.Code)
	}

	// Wrong bearer blocked.
	req2 := httptest.NewRequest(http.MethodPost, "/ingest", strings.NewReader("{}"))
	req2.Header.Set("Authorization", "Bearer nope")
	rec2 := httptest.NewRecorder()
	h.ServeHTTP(rec2, req2)
	if rec2.Code != http.StatusUnauthorized {
		t.Fatalf("wrong bearer: code = %d, want 401", rec2.Code)
	}

	// Missing bearer blocked.
	req3 := httptest.NewRequest(http.MethodPost, "/ingest", strings.NewReader("{}"))
	rec3 := httptest.NewRecorder()
	h.ServeHTTP(rec3, req3)
	if rec3.Code != http.StatusUnauthorized {
		t.Fatalf("missing bearer: code = %d, want 401", rec3.Code)
	}
}

